    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:
        int choice = 0;
        if (batch_Radio.isSelected())
        {
            choice = 1;
        }
        else if (batch_teacher_Radio.isSelected())
        {
            choice = 2;
        }
        else if (teacher_Radio.isSelected())
        {
            choice = 3;
        }
        else if (department_Radio.isSelected())
        {
            choice = 4;
        }
        else {
            choice = 5;
        }
        switch(choice) {
            case 1: JOptionPane.showMessageDialog(null, "You Selected 1");
                    BatchManagement batch_Management = new BatchManagement();
                    batch_Management.setVisible(true);
                    setVisible(false);
                    break;
            case 2: JOptionPane.showMessageDialog(null, "You Selected 2");
                    BatchTeacherManagement batch_Teacher_Management = new BatchTeacherManagement();
                    batch_Teacher_Management.setVisible(true);
                    setVisible(false);
                    break;
            case 3: JOptionPane.showMessageDialog(null, "You Selected 3");
                    TeacherManagement teacher_Management = new TeacherManagement();
                    teacher_Management.setVisible(true);
                    setVisible(false);
                    break;
            case 4: JOptionPane.showMessageDialog(null, "You Selected 4");
                    DepartmentManagement department_Management = new DepartmentManagement();
                    department_Management.setVisible(true);
                    setVisible(false);
                    break;
            case 5: JOptionPane.showMessageDialog(null, "You Selected 5");
                    StudentManagement student_Management = new StudentManagement();
                    student_Management.setVisible(true);
                    setVisible(false);
                    break;
        }
    }//GEN-LAST:event_nextActionPerformed


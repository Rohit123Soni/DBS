    private void next_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_next_buttonActionPerformed
        // TODO add your handling code here:
        int choice = 0;
        if (show_student_radio.isSelected())
        {
            choice = 1;
        }
        else if (add_student_radio.isSelected())
        {
            choice = 2;
        }
        else if (delete_student_radio.isSelected())
        {
            choice = 3;
        }
        else if (update_student_radio.isSelected())
        {
            choice = 4;
        }

        switch(choice) {
            case 1: JOptionPane.showMessageDialog(null, "You Selected 1");
                    ShowStudent show_Student = new ShowStudent();
                    show_Student.setVisible(true);
                    setVisible(false);
                    break;
            case 2: JOptionPane.showMessageDialog(null, "You Selected 2");
                    AddStudent add_Student = new AddStudent();
                    add_Student.setVisible(true);
                    setVisible(false);
                    break;
            case 3: JOptionPane.showMessageDialog(null, "You Selected 3");
                    DeleteStudent delete_Student = new DeleteStudent();
                    delete_Student.setVisible(true);
                    setVisible(false);
                    break;
            case 4: JOptionPane.showMessageDialog(null, "You Selected 4");
                    break;
        }
    }//GEN-LAST:event_next_buttonActionPerformed

